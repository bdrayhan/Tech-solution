        <h3>What topics does ISO 9001:2015 cover?</h3>
        <p>ISO 9001 is based on the plan-do-check-act methodology and provides a process-oriented approach to documenting and reviewing the structure, responsibilities, and procedures required to achieve effective quality management in an organization. Specific sections of the standard contain information on topics such as:</p>
        <ul>
          <li>Requirements for a quality management system, including documented information, planning and determining process interactions</li>
          <li>Responsibilities of management</li>
          <li>Management of resources, including human resources and an organization’s work environment</li>
          <li>Product realization, including the steps from design to delivery</li>
          <li>Measurement, analysis, and improvement of the QMS through activities like internal audits and corrective and preventive action</li>
        </ul>
