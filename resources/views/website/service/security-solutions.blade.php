        <h3>IT Penetration Testing:</h3>
        <p>Objective of the Test
          The goal of this exercise is to ensure that reasonable protection is in place for general and particular
          threats that may exist for Bank’s systems including but not limited to the following:</p>
        <ul>
          <li>To test and verify the security of the listed systems so as to ensure the effectiveness of deployed security measures.</li>
          <li>Verify the perimeter security controls.</li>
          <li>Verify the security setup and configuration of internal systems. It will include the associated networks and systems with a perspective of ensuring CIA and authenticity of data and information systems.</li>
          <li>Verify the security associated with System, Web Servers, Web applications, Database and network equipment those are used by the Bank.</li>
          <li>Identify and recommend safeguards, suited to Bank’s environment, with the aim to strengthen the level of protection of the Services.</li>
        </ul>
        <h3>Expected outcome of the assessment</h3>
        <p>The information maintained should be secured from external and internal threats (intruders, hackers, script-kiddies, theft etc.) to network, systems and applications;
          To know the process of identification of vulnerabilities that may be visible from the internet as well as from the internal network;</p>
        <p>To evaluate the Bank’s security associated with external and internal network;</p>
        <p>To comply with regulatory requirements over information security;</p>
        <p>To implement a formal, repeatable security awareness process and capability to ensure ongoing diligence in managing risks associated with Bank’s security position.
          To assess risk of different categories of data according to different levels in the company.
          To make better understand of security measures in order to ensure integrity and confidentially of data</p>
        <h3>Scope of work</h3>
        <p>The scope of work includes external penetration testing through external IP address, internal penetration testing through block of private IPs’ which connect bank’s network, systems and running applications. Also it is identifying, understanding and verifying the weaknesses, misconfigurations and vulnerabilities associated with the accessible hosts at network, operating system and application levels. The following areas are to be considered for penetration test:</p>
        <ul>
          <li>Network device configuration reviews performed through the collection and analysis of data from a sampling of network devices, such as firewalls, routers, switches and wireless access points.</li>
          <li>Network based vulnerability scanning of a sample of internal systems to assess systems, network device and applications for vulnerabilities and security weaknesses.</li>
          <li>Review of automated scan results with manual testing to reduce false positive results.</li>
          <li>Analysis of findings to determine and document information regarding risk severity level, systems impacted and business risk summary for each finding.</li>
          <li>Host discovery to identify live hosts on in-scope IP address ranges</li>
          <li>Network based vulnerability scanning of Internet accessible network devices for vulnerabilities and security weaknesses.</li>
          <li>Review of automated scan results with manual testing to reduce false positive results.</li>
          <li>Manual testing to identify vulnerabilities and security weaknesses that cannot be discovered through automated testing.</li>
          <li>Analysis of findings to determine and document information regarding risk severity level, systems impacted and business risk summary for each finding.</li>
