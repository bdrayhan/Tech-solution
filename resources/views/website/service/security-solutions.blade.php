      <h3>Penetration Testing Deliverables</h3>
      <p>Final Report of Penetration Testing will follow the following structure:</p>
      <ul>
        <li>Executive Summary</li>
        <li>Technical Findings</li>
        <li>Supplemental Data</li>
        <li>Appendices</li>
      </ul>
      <h4>Report includes:</h4>
      <ul>
        <li>The detail assessment and analysis of the weaknesses detected,</li>
        <li>Evaluate the impact and probability of exploitation associated with each security weakness,</li>
        <li>Formulate corrective actions,</li>
        <li>And provide recommendations for mitigating the risks associated with the vulnerability.
          <p>Report should be presented in logical sections as described below for management and technical audiences and are written in clear, understandable English.</p>
        </li>
        <li>Executive summary – a high-level summary of results, recommendations and the overall security posture of the assessed environment.</li>
        <li>Technical findings – a detailed, tabular breakdown of discovered vulnerabilities, their probability of exploitation, the potential impact, appropriate technical fix and mitigation advice.</li>
        <li>Supplemental data – a supplement containing the technical details of any key findings and a comprehensive analysis of critical flaws. This section also often includes sample data recovered during the exploitation of critical or high-risk vulnerabilities.</li>
        <li>Appendices – detailed records of all activities conducted by the testing team and the tools used during the engagement</li>
      </ul>
