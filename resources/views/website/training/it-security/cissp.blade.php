          <p>In this course, you will identify and reinforce the major security subjects from the eight domains of the (ISC)2 CISSP CBK. You will: Analyze components of the Security and Risk Management domain. Analyze components of the Asset Security domain. Analyze components of the Security Engineering domain. Analyze components of the Communications and Network Security domain. Analyze components of the Identity and Access Management domain. Analyze components of the Security Assessment and Testing domain. Analyze components of the Security Operations domain. Analyze components of the Software Development Security domain.</p>
