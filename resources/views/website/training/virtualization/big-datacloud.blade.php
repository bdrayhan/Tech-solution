          <p><h5>Length:</h5> 2 Days</p>
          <p>In this introductory to intermediate-level, Introduction to Cloud Computing training course, you will gain a solid understanding of the fundamental concepts and architecture of cloud computing and of the design and deployment of a cloud computing platform.</p>
          <p>According to the definition of cloud computing from the National Institute of Standards and Technology (NIST), IT services that are delivered as cloud services offer:</p>
          <ul>
            <li>A pay-as-you-go model with minimal or no initial costs</li>
            <li>Usage-based pricing, so that costs are based on actual usage</li>
            <li>Elasticity, so that users can dynamically consume more or less resources</li>
            <li>Location independence, high availability, and fault tolerance</li>
            <li>Ubiquitous access to services, where users can access services from any location using any form factor</li>
          </ul>
