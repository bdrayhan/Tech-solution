        <h2>Course Description</h2>
        <h3>Introduction</h3>
        <p>Although the PLC has been with us for more than thirty years, its role in the industrial environment is still subject to much debate and controversy. For many of its early years its role was confined to simple sequence control and interlocking. Later, however, with the advance of increasingly sophisticated microprocessor technology, analog capability – particularly PID control – became an integral part of the PLC functionality. By itself, however, the PLC was essentially blind and it was really only with the advent of the NT-based Supervisory Control And Data Acquisition (SCADA) systems that the PLC/SCADA took on a new life – competing directly with advanced DCS systems.</p>
        <p>PLC/SCADA systems are still both widely misunderstood and widely misapplied.This workshop, ‘PLC and SCADA Systems’, is designed to provide engineers and technicians with the basic theoretical and practical understanding of PLC and SCADA systems and how this can be applied to optimize their systems in terms of safety, flexibility and costs.</p>
