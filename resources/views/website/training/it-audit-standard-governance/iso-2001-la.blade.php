          <h2>ISO 2001 LA</h2>
          <a href="#"><h6>IT AUDIT, STANDARD & GOVERNANCE</h6></a>
          <h3>Course Description</h3>
          <p>Mastering the audit of an Information Security Management System (ISMS) based on ISO/IEC 27001</p>
          <h5>Summary:</h5>
          <p>This five-day intensive course enables participants to develop the necessary expertise to audit an Information Security Management System (ISMS) and to manage a team of auditors by applying widely recognized audit principles, procedures and techniques. During this training, the participant will acquire the necessary knowledge and skills to proficiently plan and perform internal and external audits in compliance with ISO 19011 the certification process according to ISO 17011.Based on practical exercises, the participant will develop the skills (mastering audit techniques) and competencies (managing audit teams and audit program, communicating with customers, conflict resolution, etc.) necessary to efficiently conduct an audit.</p>
          <h5>Who should attend?</h5>
          <ul>
            <li>Internal auditors</li>
            <li>Auditors wanting to perform and lead Information Security Management System (ISMS) certification audits</li>
            <li>Project managers or consultants wanting to master the Information Security Management System audit process</li>
            <li>CxO and Senior Managers responsible for the IT governance of an enterprise and the management of its risks</li>
            <li>Members of an information security team</li>
            <li>Expert advisors in information technology</li>
            <li>Technical experts wanting to prepare for an Information security audit function</li>
          </ul>
          <h5>What are the course objectives?</h5>
          <ul>
            <li>To acquire the expertise to perform an ISO/IEC 27001 internal audit following ISO 19011 guidelines</li>
            <li>To acquire the expertise to perform an ISO/IEC 27001 certification audit following ISO 19011 guidelines and the specifications of ISO 17021 and ISO 27006</li>
            <li>To acquire the necessary expertise to manage an ISMS audit team</li>
            <li>To understand the operation of an ISO/IEC 27001 conformant information security management system</li>
            <li>To understand the relationship between an Information Security Management System, including risk management, controls and compliance with the requirements of different stakeholders of the organization</li>
            <li>To improve the ability to analyze the internal and external environment of an organization, its risk assessment and audit decision-making</li>
          </ul>
          <h5>Course Agenda:</h5>
          <h5>Day 1: Introduction to Information Security Management System (ISMS) concepts as required by ISO/IEC 27001</h5>
          <ul>
            <li>Normative, regulatory and legal framework related to information security</li>
            <li>Fundamental principles of information security</li>
            <li>ISO/IEC 27001 certification process</li>
            <li>Information Security Management System (ISMS)</li>
            <li>Detailed presentation of the clauses 4 to 8 of ISO/IEC 27001</li>
          </ul>
          <h5>Day 2: Planning and Initiating an ISO/IEC 27001 audit</h5>
          <ul>
            <li>Fundamental audit concepts and principles</li>
            <li>Audit approach based on evidence and on risk</li>
            <li>Preparation of an ISO/IEC 27001 certification audit</li>
            <li>ISMS documentation audit</li>
            <li>Conducting an opening meeting</li>
          </ul>
          <h5>Day 3: Conducting an ISO/IEC 27001 audit</h5>
          <ul>
            <li>Communication during the audit</li>
            <li>Audit procedures: observation, document review, interview, sampling techniques, technical verification, corroboration and evaluation</li>
            <li>Audit test plans</li>
            <li>Formulation of audit findings</li>
            <li>Documentating nonconformities</li>
          </ul>
          <h5>Day 4: Concluding and ensuring the follow-up of an ISO/IEC 27001 audit</h5>
          <ul>
            <li>Audit documentation</li>
            <li>Quality review</li>
            <li>Conducting a closing meeting and conclusion of an ISO/IEC 27001 audit</li>
            <li>Evaluation of corrective action plans</li>
            <li>ISO/IEC 27001 Surveillance audit</li>
            <li>Internal audit management program</li>
          </ul>
          <h5>Day 5: Certification Exam</h5>
          <h5>Prerequisites</h5>
          <p>PECB Certified ISO/IEC 27001 Foundation Certification or basic knowledge of  ISO/IEC 27001 is recommended.</p>
          <h5>Educational approach</h5>
          <p>This training is based on both theory and practice:</p>
          <ul>
            <li>Sessions of lectures illustrated with examples based on real cases</li>
            <li>Practical exercises based on a full case study including role playings and oral presentations</li>
            <li>Review exercises to assist the exam preparation</li>
            <li>Practice test similar to the certification exam</li>
          </ul>
          <h5>Examination and Certification:</h5>
          <ul>
            <li>The “PECB Certified ISO/IEC 27001 Lead Auditor” exam fully meets the requirements of the PECB Examination and Certification Programme (ECP). The exam covers the following competence domains:
              <ul>
                <li>Domain 1: Fundamental principles and concepts of information security</li>
                <li>Domain 2: Information Security Management System (ISMS)</li>
                <li>Domain 3: Fundamental audit concepts and principles</li>
                <li>Domain 4: Preparation of an ISO/IEC 27001 audit</li>
                <li>Domain 5: Conducting an 27001 audit</li>
                <li>Domain 6: Closing an ISO/IEC 27001 audit</li>
                <li>Domain 7: Managing an ISO/IEC 27001 audit program</li>
              </ul>
            </li>
            <li>The “PECB Certified ISO/IEC 27001 Lead Auditor” exam is available in different languages (the complete list of languages can be found in the examination application form)</li>
            <li>Duration: 3 hours</li>
            <li>For more information about the exam, refer to the section on PECB Certified ISO/IEC 27001 Lead Auditor Exam</li>
            <li>After successfully completing the exam, participants can apply for the credentials of PECB Certified ISO/IEC 27001 Provisional Auditor, PECB Certified ISO/IEC 27001 Auditor or PECB Certified ISO/IEC 27001 Lead Auditor depending on their level of experience.  Those credentials are available for internal and external auditors</li>
            <li>A certificate will be issued to participants who successfully pass the exam and comply with all the other requirements related to the selected credential</li>
            <li>For more information about PECB Certified ISO/IEC 27001 certifications and the PECB certification process, refer to the section on ISO/IEC 27001 certifications</li>
          </ul>
